void UICtrl::draw()
{
  //A/B command
  switch( m_btncmd ) {
    case 0: //A
      break;
    case 1: //B
      break;
    default:
      getPic( PIC_ICON6x5 )->setFrame( 0 ); //A
      gb.display.drawImage( 60, 5, *getPic( PIC_ICON6x5 ) );
      getPic( PIC_ICON6x5 )->setFrame( 1 ); //B
      gb.display.drawImage( 70, 5, *getPic( PIC_ICON6x5 ) );
      break;
  }
  
}



